void main() {
  int number = 10; // int : 정수
  print(number);

  int number1 = 2;
  print(number1);

  print(number + number1);
  print(number - number1);
  print(number * number1);
  print(number / number1);
  print(number % number1);

  print('========================');

  double number3 = 2.5; // double : 실수
  double number4 = 0.5;

  print(number3);
  print(number4);
}
